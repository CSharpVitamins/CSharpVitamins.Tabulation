namespace CSharpVitamins.Tabulation
{
	/// <summary>
	/// The horizontal alignment of a cell.
	/// </summary>
	public enum Alignment
	{
		/// <summary>
		/// Align left (shortcut: l or L).
		/// </summary>
		Left,

		/// <summary>
		/// Align centre (shortcut: c C m or M).
		/// </summary>
		Center,

		/// <summary>
		/// Align right (shortcut: r or R).
		/// </summary>
		Right,
	}
}
